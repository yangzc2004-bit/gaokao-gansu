"""隐私声明组件 - 甘肃高考志愿推荐系统"""

import streamlit as st


def show_privacy_notice():
    """在侧边栏或页面底部显示隐私声明。"""
    with st.expander("🔒 隐私声明", expanded=False):
        st.markdown(
            """
            **甘肃高考志愿推荐系统 — 数据隐私说明**

            1. **不存储任何数据**：本系统不会存储您输入的任何个人信息，包括高考成绩、位次、姓名等。
            2. **内存计算**：所有推荐计算均在服务器内存中完成，不写入磁盘或数据库。
            3. **会话即焚**：关闭浏览器页面后，您本次输入的所有数据将被立即清除。
            4. **无第三方共享**：系统不会将您的数据传输至任何第三方服务。
            5. **开源透明**：推荐算法完全基于公开的历史录取数据，模型逻辑可审计。

            如有疑问，请联系系统管理员。
            """
        )


def show_privacy_banner():
    """在页面顶部显示简短隐私提示。"""
    st.info("🔒 本系统不存储任何考生输入数据，所有计算在内存中完成，关闭页面即清除。", icon="🔒")
